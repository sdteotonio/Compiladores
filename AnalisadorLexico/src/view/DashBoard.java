    private void abrir(){
    this.fchose = new JFileChooser();
     fchose.setFileFilter(new FileFilter() {
            @Override
            public boolean accept(File f) {
                return (f.getName().endsWith(".cs")) || f.isDirectory();
            }

            @Override
            public String getDescription() {
                return "*.cs";
            }
        });
        
        int returnVal = fchose.showOpenDialog(null);
        if (returnVal == fchose.APPROVE_OPTION) {
            File file = fchose.getSelectedFile();
            //This is where a real application would open the file.
            new ExibirCodigo(file.getAbsolutePath()).setVisible(true);
            this.dispose();
        }else{
            System.exit(0);
        }
    
    }
